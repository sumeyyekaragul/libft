#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t		len;
	char		*dest;
	int			i;

	len = ft_strlen(s1);
	i = 0;
	dest = (char *)malloc(len * sizeof(char) + 1);
	if (!dest)
		return (0);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
